    <h2>The Part Nobody's Talking About</h2>
    <div class="divider"></div>

    <p>
      A lot of people who live in major cities are originally from somewhere
      else. They moved for opportunity, for culture, for connection — and many
      are still there not because it's affordable, but because they're afraid
      of what leaving means.
    </p>

    <h3>And then there's the house.</h3>
    <p>
      "I can't afford to buy a home" has become the defining experience for
      millions of city dwellers — especially those in their 30s who did
      everything right, have good jobs, and still can't make the math work.
      That's not separate from the political story. It <em>is</em> the
      political story. The same geographic concentration that made your vote
      not count also drove housing costs to the point of exclusion.
    </p>

    <div class="stat-block">
      <div class="stat-row">
        <div>
          <span class="stat">$3,406</span>
          <span class="stat-label">NYC median rent / mo</span>
        </div>
        <div class="vs">vs</div>
        <div>
          <span class="stat text-comp">$1,515</span>
          <span class="stat-label">Columbus, OH median rent / mo</span>
        </div>
      </div>
      <p class="stat-note">
        That's <strong>$1,891/month saved — nearly $23,000 a year.</strong>
        Enough to build a down payment in three years on a house that costs
        $330K in Columbus and $718K in New York.
      </p>
    </div>

    <h3>The intellectual flight nobody talks about.</h3>
    <p>
      A lot of the people most capable of changing the communities they grew
      up in left those communities — and then slowly stopped feeling like they
      could go back. They went off to college, absorbed a set of values, built
      a new identity, and somewhere along the way started treating home like
      something to have escaped from rather than something to return to.
    </p>
    <p>
      That's a cultural failure — and Democrats own a significant share of it.
      Progressive culture allowed a quiet classism to take hold. One that
      treats leaving your hometown as graduation and staying as settling. The
      communities on the receiving end noticed. They felt the condescension
      long before anyone said it out loud. And a lot of them stopped voting
      for the party that seemed to look down on them.
    </p>

    <div class="pull-quote">
      <p>
        We turned geography into identity. If you're from rural Tennessee,
        you're supposed to vote one way. If you live in Portland, another.
        The stereotype became the expectation. The expectation became the
        pressure. We're not sorting people by where they live anymore —
        we're telling them who they're allowed to be based on their zip code.
      </p>
    </div>

    <p>
      The same way the middle class got hollowed out — squeezed from both
      sides until it stopped being a realistic place to land — the political
      middle got hollowed out too. City Republicans. Country leftists. People
      who grew up hunting and also believe in universal healthcare. They exist.
      They've always existed. But the sorting, the algorithms, the maps, and
      the cultural messaging from both sides have made them invisible.
    </p>

    <p class="fear-line">That's the fear. <strong>And it's a lie.</strong></p>
  </div>
