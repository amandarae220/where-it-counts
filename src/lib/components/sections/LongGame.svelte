  <div class="prose">
    <h2>The Map Resets in 2030</h2>
    <div class="divider"></div>

    <p>
      Every ten years, after the Census, every district in the country gets
      redrawn based on where people actually live. The next one is 2031.
    </p>
    <p>
      Politicians can't gerrymander a neighborhood that's actually changed.
      If enough people have moved in, the lines have to move with them.
      That's not a loophole — that's how it's supposed to work.
    </p>
    <p>
      State legislative maps matter most. Those are drawn by state legislators
      who use them to protect their own seats. Change who lives in a district,
      and the lines have to change with them. When a district is actually
      competitive, politicians have to work for every vote — and those are the
      people who set education policy, decide healthcare access, and control
      what goes on your ballot.
    </p>

    <div class="reset-block">
      <div class="reset-stat">
        <span class="mono" style="font-size:3rem; color: var(--color-competitive);">2031</span>
        <p>Next redistricting. Every district redrawn. Based on where people live <em>then</em>.</p>
      </div>
    </div>

    <h3>People who know their neighbors vote differently.</h3>
    <p>
      Study after study shows the same thing: when people actually know
      someone who votes differently, the hatred goes down. The geographic
      sorting of the last forty years didn't just change elections — it
      eliminated the daily contact between people with different views that
      democracy has always depended on.
    </p>
    <p>
      Moving doesn't fix that overnight. But it starts it. And the
      communities people move <em>to</em> benefit too: new residents, new
      economic activity, new perspectives — and a slow breakdown of the
      distance that made the divide feel permanent.
    </p>
  </div>
</section>

